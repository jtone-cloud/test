				  :disabled=" false  ||ro.pingjiariqi"
                  format="yyyy 年 MM 月 dd 日"
                  value-format="yyyy-MM-dd"
                  v-model="ruleForm.pingjiariqi" 
                  type="date"
                  placeholder="评价日期">
              </el-date-picker> 
          </el-form-item>
